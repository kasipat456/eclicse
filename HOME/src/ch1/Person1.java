public class Person1 {
	private String id;
	private String name;
	private Clock vehicle;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id ;
	}
	public String getname() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Clock getVehicle() {
		return vehicle;
	}
	public void setVehicle(Clock vehicle) {
		this.vehicle = vehicle;
	}
}
	
	
package ch3;

public class Person {
	protected String id;
	protected String name;
	protected int height;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getHight() {
		return height;
	}
	public void setHight(int hight) {
		this.height = hight;
	}
	
}

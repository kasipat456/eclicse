package ch2;

public class University {
	private String name;
	private String province;
	private Scholarship myScholarship;
	
	public String getname() {
		return name;
	}
	public void setname(String name) {
		this.name = name;
	}
	public String getprovince() {
		return province;
	}
	public void setprovince(String province) {
		this.province = province;
	}
	public Scholarship getmyScholarship() {
		return myScholarship;
	}
	public void setmyScholarship(Scholarship myScholarship) {
		this.myScholarship = myScholarship;
		
	}
}
	
		